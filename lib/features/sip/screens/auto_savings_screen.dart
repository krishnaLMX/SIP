  void _listenDenominations(SipState sipState) {
    final freqId = sipState.selectedFrequencyId;

    // Determine which commodity is selected
    final config = ref.read(sipConfigProvider).valueOrNull;
    final isGold = config?.commodities.any((c) =>
            c.id == sipState.selectedCommodityId &&
            c.name.toLowerCase().contains('gold')) ??
        true;

    // Listen to the correct denomination provider
    final denomProvider = isGold
        ? sipGoldDenominationsProvider(freqId)
        : sipSilverDenominationsProvider(freqId);

    ref.listen<AsyncValue<List<SipDenomination>>>(denomProvider, (prev, next) {
      if (next.hasValue && next.value != null && next.value!.isNotEmpty) {
        final denoms = next.value!;
        final popular =
            denoms.firstWhere((d) => d.isPopular, orElse: () => denoms.first);
        final String valStr = popular.value % 1 == 0
            ? popular.value.toInt().toString()
            : popular.value.toString();
        _amountController.text = valStr;
        _amountController.selection = TextSelection.fromPosition(
          TextPosition(offset: valStr.length),
        );
        ref.read(sipControllerProvider.notifier).setAmount(popular.value);
        setState(() {});
      }
    });
  }


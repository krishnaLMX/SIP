  @override
  ConsumerState<AutoSavingsScreen> createState() => _AutoSavingsScreenState();
}

class _AutoSavingsScreenState extends ConsumerState<AutoSavingsScreen>
    with SingleTickerProviderStateMixin {
  final TextEditingController _amountController = TextEditingController();
  late AnimationController _fadeController;

  @override
  void initState() {
    super.initState();
    _fadeController = AnimationController(
      vsync: this,
      duration: const Duration(milliseconds: 600),
    )..forward();
  }

  @override
  void dispose() {
    _fadeController.dispose();
    _amountController.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    final configAsync = ref.watch(sipConfigProvider);
    final sipState = ref.watch(sipControllerProvider);
    final activePlansAsync = ref.watch(sipDetailsProvider);

    // Per-commodity market status (from socket)
    // '1' = Gold 24K, '3' = Silver (socket commodity IDs).
    // Commodity absent from map = no signal yet = assume open.
    final marketStatusMap =
        ref.watch(marketStatusProvider).valueOrNull ?? const {};
    final cfgSnap = configAsync.valueOrNull;
    final isGoldSelected = cfgSnap?.commodities.any((c) =>
            c.id == sipState.selectedCommodityId &&
            c.name.toLowerCase().contains('gold')) ??
        true;
    final marketCommodityId = isGoldSelected ? '1' : '3';
    final isCurrentMarketClosed = marketStatusMap[marketCommodityId] == false;

    // Seed defaults from config on first load
    ref.listen<AsyncValue<SipConfig>>(sipConfigProvider, (prev, next) {
      next.whenData((config) {
        if (sipState.selectedFrequencyId == null &&
            config.frequencies.isNotEmpty) {
          final defaultFreq = config.frequencies.firstWhere(
            (f) => f.isDefault,
            orElse: () => config.frequencies.first,
          );
          ref.read(sipControllerProvider.notifier).setFrequency(defaultFreq.id);
        }
        if (sipState.selectedCommodityId == null &&
            config.commodities.isNotEmpty) {
          ref
              .read(sipControllerProvider.notifier)
              .setCommodity(config.commodities.first.id);
        }
      });
    });

    // Sync active plans into state for duplicate checking
    ref.listen<AsyncValue<List<SipPlanDetail>>>(sipDetailsProvider,
        (prev, next) {
      next.whenData((plans) {
        ref.read(sipControllerProvider.notifier).setActivePlans(plans);
      });
    });

    // Seed popular denomination as default input amount
    _listenDenominations(sipState);

    return Scaffold(
      backgroundColor: Colors.transparent,
      body: Column(
        children: [
          GradientHeader(
            title: 'Auto Savings',
            onBack: () => Navigator.pop(context),
          ),

          // Market Closed Amber Banner
          AnimatedSize(
            duration: const Duration(milliseconds: 300),
            curve: Curves.easeInOut,
            child: isCurrentMarketClosed
                ? Container(
                    width: double.infinity,
                    padding:
                        EdgeInsets.symmetric(horizontal: 20.w, vertical: 10.h),
                    color: const Color(0xFFFEF3C7),
                    child: Row(
                      children: [
                        Icon(Icons.warning_amber_rounded,
                            color: const Color(0xFFB45309), size: 16.sp),
                        SizedBox(width: 8.w),
                        Expanded(
                          child: Text(
                            '${isGoldSelected ? 'Gold' : 'Silver'} market is closed. Auto Savings will resume when market opens.',
                            style: TextStyle(
                              fontSize: 11.sp,
                              fontWeight: FontWeight.w600,
                              color: const Color(0xFF92400E),
                              height: 1.4,
                            ),
                          ),
                        ),
                      ],
                    ),
                  )
                : const SizedBox.shrink(),
          ),

          Expanded(
            child: configAsync.when(
              loading: () => Center(
                child: CircularProgressIndicator(
                  color: const Color(0xFF064E3B),
                  strokeWidth: 2.5,
                ),
              ),
              error: (err, _) => _buildErrorState(err.toString()),
              data: (config) => FadeTransition(
                opacity: _fadeController,
                child: _buildContent(config, sipState, activePlansAsync),
              ),
            ),
          ),
        ],
      ),
      bottomNavigationBar: configAsync.hasValue
          ? (sipState.selectedFrequencyId != null &&
                  sipState.hasActivePlanForFrequency(
                      sipState.selectedFrequencyId!,
                      commodityId: sipState.selectedCommodityId)
              ? _buildManageButton(configAsync.value!, sipState,
                  isMarketClosed: isCurrentMarketClosed)
              : _buildBottomAction(configAsync.value!, sipState,
                  isMarketClosed: isCurrentMarketClosed))
          : null,
    );
  }

